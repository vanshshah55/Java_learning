public class a3_datatypes{
    public static void main(String[] args){
        int mynum = 5;               // Integer (whole number)
        float myfloat = 5.99f;    // Floating point number (include letter f)
        char myletter = 'V';     // Character (single quotes)
        boolean mybool = true;  // Boolean (true or false)
        String mytext = "Hello"; // String (double quotes)
        byte mybyte = 100; // Byte (-128 to 127)
        short myshort = 5000; // Short (-32,768 to 32,767)
        Long mylong = 15000000000L; // Long (include letter L)
        double mydouble = 19.99d; // Double (include letter d)
        System.out.println(mynum);
        System.out.println(myfloat); 
        System.out.println(myletter);
        System.out.println(mybool);
        System.out.println(mytext);
        System.out.println(mybyte);
        System.out.println(myshort);
        System.out.println(mylong);
        System.out.println(mydouble);

    }
}